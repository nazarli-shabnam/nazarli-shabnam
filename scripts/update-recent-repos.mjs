/**
 * Rewrites the <!-- recent-repos start --> ... <!-- recent-repos end --> block in README.md
 * with public repos you've had activity on in the last N days (default 5).
 *
 * Uses GET /users/{login}/events/public — only public activity; private repos never appear.
 * Signals: PushEvent, PullRequestEvent (opened/closed/edited on a repo).
 */

import fs from "node:fs/promises";
import path from "node:path";

const README_PATH = path.join(process.cwd(), "README.md");
const START = "<!-- recent-repos start -->";
const END = "<!-- recent-repos end -->";

const DAYS = Number(process.env.RECENT_REPOS_DAYS || "5");
const USERNAME = process.env.GITHUB_USERNAME || process.env.GITHUB_REPOSITORY_OWNER;
const TOKEN = process.env.GITHUB_TOKEN;

/** Comma-separated `owner/repo` to skip. If unset, skips `username/username` (profile README noise). Set to empty to skip nothing. */
function excludeSet() {
  const raw = process.env.RECENT_REPOS_EXCLUDE;
  const list =
    raw !== undefined
      ? raw.split(",")
      : [`${USERNAME}/${USERNAME}`];
  return new Set(list.map((s) => s.trim()).filter(Boolean));
}

if (!USERNAME) {
  console.error("Missing GITHUB_USERNAME (or set GITHUB_REPOSITORY_OWNER).");
  process.exit(1);
}

function cutoffMs() {
  return Date.now() - DAYS * 24 * 60 * 60 * 1000;
}

async function fetchPublicEventsPage(page) {
  const url = new URL(`https://api.github.com/users/${encodeURIComponent(USERNAME)}/events/public`);
  url.searchParams.set("per_page", "100");
  url.searchParams.set("page", String(page));

  const headers = {
    Accept: "application/vnd.github+json",
    "X-GitHub-Api-Version": "2022-11-28",
  };
  if (TOKEN) {
    headers.Authorization = `Bearer ${TOKEN}`;
  }

  const res = await fetch(url, { headers });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`GitHub API ${res.status}: ${text.slice(0, 500)}`);
  }
  return res.json();
}

function repoNameFromEvent(ev) {
  return ev?.repo?.name || null;
}

function considerEvent(ev, cutoff) {
  const t = new Date(ev.created_at).getTime();
  if (t < cutoff) return { inWindow: false, repo: null };

  if (ev.type === "PushEvent") {
    const name = repoNameFromEvent(ev);
    return { inWindow: true, repo: name };
  }

  if (ev.type === "PullRequestEvent") {
    const name = repoNameFromEvent(ev);
    return { inWindow: true, repo: name };
  }

  return { inWindow: true, repo: null };
}

async function collectRecentRepos() {
  const cutoff = cutoffMs();
  const excluded = excludeSet();
  /** @type {Map<string, number>} */
  const lastSeen = new Map();

  // GitHub returns at most ~300 recent public events total across pages.
  for (let page = 1; page <= 10; page++) {
    const events = await fetchPublicEventsPage(page);
    if (!Array.isArray(events) || events.length === 0) break;

    const oldestOnPage = new Date(events[events.length - 1].created_at).getTime();

    for (const ev of events) {
      const t = new Date(ev.created_at).getTime();
      const { inWindow, repo } = considerEvent(ev, cutoff);
      if (!inWindow) continue;
      if (!repo || excluded.has(repo)) continue;
      lastSeen.set(repo, Math.max(lastSeen.get(repo) || 0, t));
    }

    if (oldestOnPage < cutoff) break;
    if (events.length < 100) break;
  }

  return [...lastSeen.entries()]
    .sort((a, b) => b[1] - a[1])
    .map(([name]) => name);
}

function buildBlock(repos) {
  const since = new Date(Date.now() - DAYS * 24 * 60 * 60 * 1000);
  const sinceStr = since.toISOString().slice(0, 10);

  const lines = [
    START,
    `<!-- generated by scripts/update-recent-repos.mjs (${DAYS}d window, since ${sinceStr}) -->`,
    "",
    `Public activity in the last **${DAYS} days**.`,
    "",
  ];

  if (repos.length === 0) {
    lines.push(
      "_No qualifying public activity in this window — or the feed hasn’t picked up pushes yet._",
      "",
      "*(This section updates on the daily workflow run.)*",
    );
  } else {
    for (const fullName of repos) {
      const url = `https://github.com/${fullName}`;
      lines.push(`- [${fullName}](${url})`);
    }
  }

  lines.push("", END);
  return lines.join("\n");
}

async function main() {
  const repos = await collectRecentRepos();
  let readme = await fs.readFile(README_PATH, "utf8");

  if (!readme.includes(START) || !readme.includes(END)) {
    console.error(`README.md must contain ${START} and ${END} markers.`);
    process.exit(1);
  }

  const block = buildBlock(repos);
  readme = readme.replace(new RegExp(`${escapeRe(START)}[\\s\\S]*?${escapeRe(END)}`, "m"), block);

  await fs.writeFile(README_PATH, readme, "utf8");
  console.log(`Updated recent repos (${repos.length}): ${repos.join(", ") || "(none)"}`);
}

function escapeRe(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

await main();
